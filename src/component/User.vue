
<template>
 
  <form v-on:submit.prevent="addItem">
    <p>Add item</p>
    <p>Item name: <input type="text" required v-model="itemName"></p>
    <p>How many: <input type="number" v-model="itemNumber"></p>
    <button type="submit">Add item</button>
  </form>

  <p>Shopping list:</p>
 
    <p v-for="item in shoppingList"><li>{{item.name}} {{item.number}} </li></p>

</template>


<script>
export default{
    data() {
        return {
            itemName: null,
            itemNumber: null,
            shoppingList: 
              [
            ]
        }
    },
    methods: {
        addItem() {
            let item = {
                name: this.itemName,
                number: this.itemNumber
            }
            this.shoppingList.push(item)
            this.itemName = null
            this.itemNumber = null
        }
    }

}

</script>


